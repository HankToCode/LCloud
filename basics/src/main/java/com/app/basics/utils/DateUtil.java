    /**
     * 获取起止日期
     *
     * @param format 需要显示的日期格式
     * @param date   需要参照的日期
     * @param n      最近n周
     * @param option 0 开始日期；1 结束日期
     * @param k      0 包含本周 1 不包含本周
     * @return
     */
    public static String getFromToDate(String format, Date date, int n, int option, int k) {
        SimpleDateFormat sdf = new SimpleDateFormat(format);
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(date);
        int dayOfWeek = calendar.get(Calendar.DAY_OF_WEEK) - 1;
        int offset = 0 == option ? 1 - dayOfWeek : 7 - dayOfWeek;
        int amount = 0 == option ? offset - (n - 1 + k) * 7 : offset - k * 7;
        calendar.add(Calendar.DATE, amount);
        return sdf.format(calendar.getTime());
    }

    public static String str2TimeStamp(String date, String format) {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat(format);
            return String.valueOf(sdf.parse(date).getTime() / 1000);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return "";
    }

    /**
     * 获取时间间隔
     *
     * @param endStr
     * @return
     */
    public static long getTimeInterval(String startStr, String endStr) {

        SimpleDateFormat sdf = new SimpleDateFormat(FORMAT);
        try {
            Date curDate = sdf.parse(startStr);
            Date endDate = sdf.parse(endStr);
            return endDate.getTime() - curDate.getTime();
        } catch (ParseException e) {
            e.printStackTrace();
        }
        return 0;

    }


