    /**
     * 获得天- 时 - 分 - 秒  时间差
     *
     * @param begin
     * @param end
     * @return
     */
    public static Long[] getDateDiff(Date begin, Date end) {
        if (begin == null || end == null) return new Long[4];
        Long[] longs = new Long[4];
        long diff = begin.getTime() - end.getTime();//这样得到的差值是微秒级别
        long days = diff / (1000 * 60 * 60 * 24);
        long hours = (diff - days * (1000 * 60 * 60 * 24)) / (1000 * 60 * 60);
        long minutes = (diff - days * (1000 * 60 * 60 * 24) - hours * (1000 * 60 * 60)) / (1000 * 60);
        long seconds = (diff - days * (1000 * 60 * 60 * 24) - hours * (1000 * 60 * 60) - minutes * (1000 * 60)) / 1000;
        longs[0] = days < 0 ? 0 : days;
        longs[1] = hours < 0 ? 0 : hours;
        longs[2] = minutes < 0 ? 0 : minutes;
        longs[3] = seconds < 0 ? 0 : seconds;
        return longs;
    }

