    /**
     * 向手机写文件
     *
     * @param buffer
     * @param folder
     * @param fileName
     * @return
     */
    public static boolean writeFile(byte[] buffer, String folder,
                                    String fileName) {
        boolean writeSucc = false;
        File fileDir = new File(folder.substring(0,
                folder.lastIndexOf(File.separator)));
        if (!fileDir.exists()) {
            fileDir.mkdirs();
        }
        File file = new File(folder + fileName);
        FileOutputStream out = null;
        try {
            out = new FileOutputStream(file);
            out.write(buffer);
            writeSucc = true;
        } catch (Exception e) {
            e.printStackTrace();
            writeSucc = false;
        } finally {
            try {
                out.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        return writeSucc;
    }

